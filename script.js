 
 // Formas diferentes de criar variáveis:
 // 1 - Variável global (Simplismente coloca o nome da variável)
 // 2 - Var (Forma antiga de fazer)
 // 3 - Let (Pode ser alterado o valor de dentro)
 // 4 - Const (Não pode alterar o valor da variável)
 
// Essa é uma variável que guarda o valor da minha idade atual 
 let idade = 19
 
 // A data que eu faço aniversário
const dataAniversario = "30 de Janeiro"

// Imprimindo a minha idade atual no console
 // Console.log("Minha idade atual: ", idade)
 console.log(`Minha idade atual e ${idade}`);

 // idade depois que fiz aniversário
 idade = 20
 // Imprimindo minha idade atual novamente
 console.log(`Minha idade depois do aniversário e ${idade}`);

 // Imprimindo minha data de aniversário
 console.log(`Meu aniversário e ${dataAniversario}`);